package com.springcore.standalone;

import java.util.List;

public class StandAlone {
	private List list;

	public List getList() {
		return list;
	}

	public void setList(List list) {
		this.list = list;
	}

	@Override
	public String toString() {
		return "StandAlone [list=" + list + "]";
	}
	
	
}
